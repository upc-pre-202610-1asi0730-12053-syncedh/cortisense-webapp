const validPairs = pairs.filter((pair) => Array.isArray(pair) && pair.length === 2 && pair[0] && pair[1])
const toEn = Object.fromEntries(validPairs)
const toEs = Object.fromEntries(validPairs.map(([es, en]) => [en, es]))

function translateValue (value, locale) {
  if (!value || !value.trim()) return value
  const leading = value.match(/^\s*/)?.[0] || ''
  const trailing = value.match(/\s*$/)?.[0] || ''
  const core = value.trim()
  const dictionary = locale === 'en' ? toEn : toEs
  const exact = dictionary[core]
  if (exact) return `${leading}${exact}${trailing}`

  // Some templates split sentences around <strong> tags, and some labels are
  // rendered by dynamic lists. Apply phrase-level replacements without touching
  // names, e-mails, or data values that do not match known UI strings.
  let translatedCore = core
  const entries = Object.entries(dictionary).sort((a, b) => b[0].length - a[0].length)
  const escapeRegExp = (text) => text.replace(/[.*+?^${}()|[\]\\]/g, '\\$&')
  for (const [source, target] of entries) {
    if (source.length < 4) continue
    // Phrase-level replacements are useful for split templates, but single-word
    // replacements can corrupt words like "Personalized" -> "Staffized".
    if (!/[\s.,:;()¿?¡!—/-]/.test(source)) continue
    const pattern = new RegExp(escapeRegExp(source), 'g')
    if (pattern.test(translatedCore)) translatedCore = translatedCore.replace(pattern, target)
  }
  return translatedCore !== core ? `${leading}${translatedCore}${trailing}` : value
}

function applyStaticTranslations (locale) {
  const root = document.getElementById('app')
  if (!root) return
  const walker = document.createTreeWalker(root, NodeFilter.SHOW_TEXT, {
    acceptNode (node) {
      const parent = node.parentElement
      if (!parent || ['SCRIPT', 'STYLE', 'TEXTAREA'].includes(parent.tagName)) return NodeFilter.FILTER_REJECT
      return NodeFilter.FILTER_ACCEPT
    }
  })
  const textNodes = []
  while (walker.nextNode()) textNodes.push(walker.currentNode)
  for (const node of textNodes) {
    const next = translateValue(node.nodeValue, locale)
    if (next !== node.nodeValue) node.nodeValue = next
  }
  root.querySelectorAll('input[placeholder], textarea[placeholder]').forEach(el => {
    const next = translateValue(el.getAttribute('placeholder'), locale)
    if (next !== el.getAttribute('placeholder')) el.setAttribute('placeholder', next)
  })
}

export function startStaticI18nSync (i18n, router) {
  let pending = null
  const currentLocale = () => typeof i18n.global.locale === 'string' ? i18n.global.locale : i18n.global.locale.value
  const schedule = () => {
    clearTimeout(pending)
    pending = setTimeout(() => applyStaticTranslations(currentLocale()), 0)
  }
  schedule()
  watch(() => currentLocale(), schedule)
  router.afterEach(schedule)
  const observer = new MutationObserver(schedule)
  observer.observe(document.getElementById('app'), { childList: true, subtree: true })
}
